#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*pntr;

	pntr = s;
	i = 0;
	if (n == 0)
		return ;
	while (i < n)
	{
		pntr[i] = 0;
		i++;
	}
}
