#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*str;

	if (!(str = (char*)malloc(sizeof(char) * (size + 1))))
		return (NULL);
	size++;
	ft_bzero(str, size);
	ft_memset(str, '\0', size);
	return (str);
}
